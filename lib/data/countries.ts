/**
 * Get a country by its dial code
 * Returns the first match found (some countries share codes like +1)
 */
export function getCountryByCode(code: string): Country | undefined {
  // First check popular countries for faster lookup
  const popular = POPULAR_COUNTRIES.find((c) => c.code === code);
  if (popular) return popular;

  // Fall back to all countries
  return ALL_COUNTRIES.find((c) => c.code === code);
}

/**
 * Get a country by its ISO2 code
 */
export function getCountryByIso2(iso2: string): Country | undefined {
  return ALL_COUNTRIES.find((c) => c.iso2 === iso2.toUpperCase());
}
